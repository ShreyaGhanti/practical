package com;

public class SwitchTest {

	public static void main(String[] args) {
		int num=40;
		switch(num) {
		case 1 :System.out.println("12");
		break;
		case 12 :System.out.println("15");
		break;
		case 16:System.out.println("13");
		break;
		case 40 :System.out.println("very good");
		break;
		default:System.out.println("number is not within the range");
		
		}

	}

}

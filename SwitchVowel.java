
public class SwitchVowel {

	public static void main(String[] args) {
		char oh ='e';
		switch(oh) {
		case 'y':System.out.println("its not a vowel");
		break;
		case 'e':System.out.println("its  a vowel");
		break;
		case 'g':System.out.println("its not a vowel");
		break;
		case 'm':System.out.println("its not a vowel");
		break;
		}

	}

}

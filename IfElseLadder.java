
public class IfElseLadder {

	public static void main(String[] args) {
	String name ="Shreya";
	if(name=="priya") {
		System.out.println("name is priya");
	}else if (name=="puja") {
		System.out.println("name is puja");
	}else if (name=="pritam") {
		System.out.println("name is pritam");
	}else {
		System.out.println(name );
	}
	}
	}



